#include <iostream>
using namespace std;

int main()
{
   // Pós-Incremento
   int c = 5;
   cout << c << endl;
   cout << c++ << endl;
   cout << c << endl;

   cout << endl;

   // Pré-Incremento
   c = 5;
   cout << c << endl;
   cout << ++c << endl;
   cout << c << endl;

}
